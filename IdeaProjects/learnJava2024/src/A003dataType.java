public class A003dataType {
    public static void main(String[] args) {
        // 整型
        byte b = 1;
        short s = 2;
        int i = 3;
        long l = 4;

        // 浮点型
        float f = 1.1f;
        double d = 2.2;

        // 字符型
        char c = 'a';

        // 布尔型
        boolean bool = true;

        // 字符串
        String str = "hello";

        // 数组
        int[] arr = {1, 2, 3};

        System.out.println("byte: " + b + ", short: " + s + ", int: " + i + ", long: " + l + ", float: " + f + ", double: " + d + ", char: " + c + ", boolean: " + bool + ", String: " + str + ", array: " + arr);
    }
}
